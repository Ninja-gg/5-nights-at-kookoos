def get_vent_direction(guy4: Sprite):
    global vx, vy
    vx = guy4.vx
    vy = guy4.vy
    if abs(vx) < 0.001 and abs(vy) < 0.001:
        return 0
    if abs(vx) > abs(vy):
        if vx < 0:
            return 1
        else:
            return 2
    else:
        if vy < 0:
            return 3
        else:
            return 4
