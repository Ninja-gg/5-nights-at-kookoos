def Update_animatronic(guy3: Sprite, path2: List[Sprite]):
    global Target_waypoint_type, wpat, waitTime
    Target_waypoint_type = sprites.read_data_number(path2[sprites.read_data_number(guy3, "Target")], "Type")
    if not (guy3.overlaps_with(path2[sprites.read_data_number(guy3, "Target")])):
        guy3.follow(path2[sprites.read_data_number(guy3, "Target")])
        if sprites.read_data_boolean(guy3, "In Vent"):
            sprites.set_data_number(guy3, "Direction", get_vent_direction(guy3))
        return
    wpat = sprites.read_data_number(guy3, "WPAT")
    if wpat == 0:
        sprites.set_data_number(guy3, "WPAT", game.runtime())
        return
    waitTime = game.runtime() - wpat
    if Target_waypoint_type == 0:
        if waitTime < WP_0_minimum_wait_time:
            return
        Move_on_to_next_waypoint(guy3, path2, target_index2)
    elif Target_waypoint_type == 1:
        if waitTime < WP_1_minimum_wait_time:
            return
        if not (WP_1_Go_or_NoGo_TRUE_is_move_on):
            return
        Move_on_to_next_waypoint(guy3, path2, target_index2)
    elif Target_waypoint_type == 2:
        if sprites.read_data_number(guy3, "Direction") == 0:
            sprites.set_data_number(guy3, "Direction", 3)
            return
        if waitTime < WP_2_minimum_wait_time:
            return
        sprites.set_data_boolean(guy3, "In Vent", True)
        Teleport_to_waypoint(guy3, path2, sprites.read_data_number(guy3, "Target") + 1)
        return
    elif Target_waypoint_type == 3:
        if waitTime < WP_3_minimum_wait_time:
            return
        sprites.set_data_boolean(guy3, "In Vent", False)
        sprites.set_data_number(guy3, "Direction", 0)
        Teleport_to_waypoint(guy3, path2, sprites.read_data_number(guy3, "Target") + 1)
        return
    else:
        console.log_value("TargetWaypointType", Target_waypoint_type)
